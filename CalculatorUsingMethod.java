/*
 * this is a simple java program to make a calculator by using
 * method
 */
public class CalculatorUsingMethod {
	
	//this method is to add two numbers
	public void Add(double number1,double number2){
		System.out.println("Addition of these numbers is "+(number1+number2));
	}
	
	//this method is to subtract two numbers
	public void Subtract(double number1,double number2){
		System.out.println("Subtraction of these numbers is "+(number1-number2));
	}
	
	//this method is to multiply two numbers
	public void Multiply(double number1,double number2){
		System.out.println("Multiplication of these numbers is "+(number1*number2));
	}
	
	//this method is to divide two numbers
	public void Divide(double number1,double number2){
		System.out.println("Division of these numbers is "+(number1/number2));
	}
}
